    void loadOpenForm() {
        for (double i = 0.0; i < 1.0; i = i + 0.1) {
            String val = i + "";
            float f = Float.parseFloat(val);
            this.setOpacity(f);
            try {
                Thread.sleep(70);
            } catch (Exception e) {
            }
        }
    }

    void hover(int so, JPanel pnl) {
        if (so == 1) {
            pnl.setBackground(new Color(122, 89, 186));
            pnl.setCursor(new Cursor(HAND_CURSOR));
        } else {
            pnl.setBackground(new Color(102, 153, 255));
        }
    }

    void changeIcon(int so, JButton btn, String url) {
        if (so == 1) {
            Image img = getToolkit().createImage(url);
            btn.setIcon(new ImageIcon(img));
            btn.setCursor(new Cursor(HAND_CURSOR));
        } else {
            Image img = getToolkit().createImage(url);
            btn.setIcon(new ImageIcon(img));
        }
    }

    void menu() {
        showHideMenu++;
        if (showHideMenu % 2 == 0) {
            pnlNavigation.setBackground(new Color(55, 38, 91));
            pnlMenu.setSize(60, 800);
            btnMenu.setLocation(80, 20);
            lblSlogan.setLocation(130, 23);
        } else {
            pnlNavigation.setBackground(new Color(83, 62, 128));
            pnlMenu.setSize(230, 800);
            btnMenu.setLocation(250, 20);
            lblSlogan.setLocation(300, 23);
        }
    }

    void showHideSubMenu(int so, JPanel panel) {
        so++;
        if (so % 2 == 0) {
            pnlSubMenu.setVisible(true);
        } else {
            pnlSubMenu.setVisible(false);
        }
        JPanel[] pnl = new JPanel[]{pnlSubThanhToan, pnlSubVe, pnlSubChuyenBay};
        for (int i = 0; i < pnl.length; i++) {
            pnl[i].setVisible(false);
        }
        panel.setVisible(true);
    }


    void checkUser() {
        new Thread() {
            @Override
            public void run() {
                while (true) {
                    try {
                        chucVu = frmLogin.vt;
                        String ten = new MainDAO().getHoTen(frmLogin.tenDN);
                        if (ten.isEmpty()) {
                            lblUser.setText("NULL");
                        } else {
                            lblUser.setText(ten);
                            lblChucVu.setText(frmLogin.vt);
                        }
                        Thread.sleep(100);
                    } catch (Exception e) {
                        e.printStackTrace();
                    }
                }
            }
        }.start();
    }

